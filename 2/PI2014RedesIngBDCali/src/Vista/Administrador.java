    private void jButton_ConsultingUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConsultingUserActionPerformed
        ArrayList<String> resp = client.cargaUsuarioC();
        String[] nombres = {"Usuario", "Password", "Tipo"};
        DefaultTableModel dtm = new DefaultTableModel(nombres, resp.size());
        dtm = (DefaultTableModel) this.jTable_Usuario.getModel();
        this.jTable_Usuario.removeAll();

        for (String s : resp)
        {
            String[] columnasT = s.split(";");
            dtm.addRow(new Object[]{columnasT[1], columnasT[2], columnasT[3]});
        }
    }//GEN-LAST:event_jButton_ConsultingUserActionPerformed

